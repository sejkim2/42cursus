#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	void	*p;
	size_t	i;

	p = b;
	i = 0;
	while (i < len)
	{
		*((unsigned char *)p + i) = 0;
		i++;
	}
	return (b);
}
